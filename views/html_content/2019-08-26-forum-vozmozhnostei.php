<p>Новосибирская региональная общественная организация по защите прав и законных интересов инвалидов «МиРа» принимает участи в работе IV Форума «Новосибирск – город безграничных возможностей 2019». До начала мероприятия, нас и других участников пригласили на утренний чай с мэром города. Были представлены те, кто работают над проведением форума - ведущие специалисты в вопросах доступной среды, бизнес-тренеры, ученые и представители организаций, занимающихся работой с людьми с ограниченными возможностями. Форум собрал специалистов из многих регионов страны и ближнего зарубежья. Открывая мероприятие, мэр Новосибирска Анатолий Локоть подчеркнул, что эта работа направлена, прежде всего, на стирание граней между людьми, создание в регионе безбарьерной среды. <br/>
Депутат Законодательного Собрания Новосибирской области, председатель Комитета по социальной политике, здравоохранению, охране труда и занятости населения Игорь Гришунин, отметил, что форум выходит на международный уровень. «Сегодня мы приглашаем участников из ближнего зарубежья, что бы обменяться опытом, знаниями и возможностями в области социальной политики», - расказал парламентарий. <br/>
Не первый год в новосибирском форуме принимает участие президент международной благотворительной организации «Справедливая помощь Доктора Лизы», исполнительный директор фонда поддержки слепоглухих <span class="nowrap">«Со-Единение»</span> Татьяна Константинова.</p>

