<p>Побывали на выставке проектов виртуальной и дополненной реальности «Слепые в большом городе» и на презентации костюма GERT. Последний был изобретен в Великобритании немецким дизайнером Вольфангом Моллом для моделирования возрастных изменений человеческого организма. Изначально они использовались для подготовки медицинского персонала в университетах, но оказались полезными в сфере торговли и услуг. Костюм состоит из отдельных компонентов, которые крепятся на одежду испытуемых — жилета, нарукавников и наколенников, перчаток, спецобуви, а также включает особые очки и наушники. Работая вместе, эти модули создают эффект снижения сенсомоторной активности, в частности демонстрируют помутнение хрусталика глаза, сужение поля зрения, потерю высокой частоты слуха, ограничение подвижности головы, тугоподвижность суставов, потерю физической силы, уменьшение способности сжатия и захвата предметов руками, снижение навыков координации. Костюм весит около 20 килограммов, что также помогает продемонстрировать проблемы со здоровьем. 
Один экземпляр сегодня тестируется в Новосибирске для работников банков и социальных учреждений. Считается, что человек, побывавший в этом костюме, может испытать на себе состояние больного или инвалида. А побывав на его месте, специалист с пониманием будет относиться к проблемам людей с ограничениями здоровья. </p>

