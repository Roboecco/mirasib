<h3>Мировцы приняли участие в соревновании военно-патриотических объединений <span class="nowrap">«Наследие Героев»</span></h3>

<p>Сбор был назначен на 8 часов утра в офисе Ленинского отделения партии «ЕДИНАЯ РОССИЯ» <span class="nowrap">на Римского–Корсакова, 10.</span> Мальчишки и девчонки из десяти команд, представляющих школы Новосибирска и области, упаковывали свои рюкзачки, проверяли – все ли собрали? По всему видно - готовились к соревнованиям основательно. Перед посадкой в автобус, интересуюсь у спортивного инструктора команды «МиРа» Александра Кисилева, волнуются ли ребята?<br/>
- На мой взгляд, они подготовлены. Все дети спортивные – девочки легкоатлетки, мальчики тоже занимаются спортом. Возраст от 13 до 15 лет. Капитан команды Михаил Мамедов – борец, принимал участие в соревнованиях российского уровня. Трасса не простая, но на всех этапах присутствуют инструкторы - думаю, мое участие даже и не понадобится. Настроение хорошее, все улыбаются!</p>

<p>Уже не первый год инициатором проведения соревнований «Наследие героев» среди любительских и профессиональных военно-патриотических объединений, выступает депутат Законодательного Собрания Новосибирской области, Секретарь Местного отделения Партии “ЕДИНАЯ РОССИЯ” Ленинского района Виктор Кушнир. В этот раз они посвящены памяти Героя Советского Союза Александра Яковлевича Анцупова. По словам Виктора Васильевича, военно-патриотическое воспитание молодежи возрождается в России, и это правильно.</p>

