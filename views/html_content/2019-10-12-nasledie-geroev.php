- Несмотря на усталость, мы очень довольны и счастливы, - рассказывает наша активистка Арина Близневская. В команде царила товарищеская атмосфера, чувство локтя - на всех этапах мы помогали другу другу. Раньше мне казалось, что важнее всего победа. Теперь поняла – ничто не сравнится с ощущениями, которые испытываешь в момент спортивных баталий.</p>

