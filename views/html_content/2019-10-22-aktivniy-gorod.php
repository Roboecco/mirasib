<p>Открывая форум, мэр Новосибирска Анатолий Локоть отметил, что поднимаемые на мероприятии вопросы актуальны, интересны и полезны не только для общественной деятельности Новосибирска, но и для развития самого города. Если посмотреть на историю города, он возник не на пустом месте, а создавался и развивался, в том числе и благодаря социальной инициативе. Вот и сегодня жизнь областного центра в этом направлении кипит, в ней постоянно что-то происходит, меняется в лучшую сторону – благодаря новосибирцам развиваются территории общественного самоуправления (ТОС).</p>

