<p>Перед пленарным совещанием, которое состоялось в конференц-зале Маринс Парк Отеля “Новосибирск”, в вагоне метро Новосибирского метрополитена на станции «Площадь Гарина-Михайловского», была открыта выставка «Активный город» с презентациями достижений общественных организаций областного центра. Особого внимания заслуживает экспозиция, посвященная деятельности национально-культурных центров.</p>

