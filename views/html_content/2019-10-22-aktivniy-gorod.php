<p>На форуме обсуждаются вопросы развития основных направлений деятельности городских сообществ:<br/>
- «Город соседей» - вопросы комплексного развития территорий ТОС, лучшие практики деятельности ТОС, перспектива развития;<br/>
- «Город патриотов» - проекты, направленные на патриотическое воспитание, городской патриотизм, диалог поколений, уроки истории;<br/>
- «Город друзей» - межнациональные, межконфессиональные отношения, международная дипломатия;<br/>
- «Город добрых» - благотворительность, добровольчество, социальные услуги и практики.</p>
<p>Также в рамках форума пройдет серия специальных мероприятий: семинары, круглые столы, которые проводятся по инициативе общественных организаций.</p>

<div class="img">
	<img src="<?= $imageFolder ?>03.jpg" alt="" width="100%" height="auto" />
