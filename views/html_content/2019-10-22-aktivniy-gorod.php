<p>На предыдущих форумах «Активный город», общественными объединениями поднимался вопрос о взаимодействии со средствами массовой информации. Они считали, что СМИ недостаточно освещают их деятельность. В этом году решено создать секцию по обучению общественных организаций правильному взаимодействию с журналистами. О том, как будет проходить семинар на форуме, рассказала журналист Информационного агентства «Новосибирск» Александра Медведенко.</p>

