<h3>Актуальные вопросы деятельности городских сообществ обсуждают на <span class="nowrap">V форуме</span> <span class="nowrap">«Активный город»</span></h3>

<p>В пятый раз в Новосибирске стартовал форум городских сообществ «Активный город».<br/>
Вместе с активистами некоммерческих организаций, территориальных общественных самоуправлений, национально-культурных организаций, объединений, инициативных групп ответы на актуальные вопросы деятельности городских сообществ ищут ученые, депутаты, представители исполнительной  власти, новосибирские и российские эксперты. Принимают участие в работе форума и представители НРОО «МиРа».</p>

