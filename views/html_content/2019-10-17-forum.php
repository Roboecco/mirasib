<p>Ежегодный региональный форум "Гражданский диалог» состоялся на площадке МВК “Новосибирск-Экспоцентр” 17 октября. В этом году в его работе принимали участие и представители НРОО «МиРа».<br/>
Цель форума - обсуждение перспектив и возможностей взаимодействия органов власти, общественных организаций и бизнеса для решения социально-экономических задач.<br/>
«Гражданский диалог» пригласил для работы на форуме активистов местных общественных организаций, членов и экспертов Общественной палаты Российской Федерации, членов Общественной палаты Новосибирской области, представителей законодательной и исполнительной власти Новосибирской области, региональных и федеральных экспертов, представителей социально-ответственного бизнеса, победителей федеральных, областных и муниципальных грантовых программ.</p>

