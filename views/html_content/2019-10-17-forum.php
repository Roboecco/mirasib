<p>По мнению мэра Новосибирска Анатолия Локтя, социальное предпринимательство давно заняло свою нишу в развитии среднего и малого бизнеса. Под эту федеральную программу государство выделяет немалые средства. И финансирование это будет вестись не год и не два, а на постоянной основе. Нужно активнее проявлять инициативу и приходить во власть для реализации задуманного с конкретными идеями, задачами и предложениями.</p>

