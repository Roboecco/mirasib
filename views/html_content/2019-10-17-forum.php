<p>В форуме также приняли участие представители фондов Потанина, Тимченко, "Справедливая помощь Доктора Лизы",  других структур бизнеса и органов власти. Обсуждался опыт внедрения инновационных социальных технологий, возможности расширения спектра социальных услуг социально-ориентированных некоммерческих организаций, их независимой оценки и повышения качества.</p>

