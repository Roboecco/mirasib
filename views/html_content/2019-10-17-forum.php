<p>Открывая дискуссионную площадку «Бизнес, общество и власть: лучшие практики и перспективы партнерства», губернатор региона Андрей Травников подчеркнул, что форум не случайно проходит уже в девятый раз. Вопросы социального предпринимательства актуальны и требуют комплексного подхода к их рассмотрению. “Такие площадки, как форум «Гражданский диалог» - это возможность донести до властных структур в числе других вопросов и злободневные проблемы развития социального предпринимательства. В свою очередь, мы готовы поддерживать и развивать гражданскую, общественную активность. Считаю, что активные общественные, некоммерческие организации, негосударственный сектор - это конкурентное преимущество Новосибирской области” - отметил губернатор.</p>

