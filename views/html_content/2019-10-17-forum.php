	<p>«Участие НРОО «МиРа» в данном мероприятии очень полезно, - считает президент Новосибирской региональной общественной организации по защите прав и законных интересов инвалидов «МиРа» Владимир Кулекин. Работа почти всех площадок форума, в той или иной степени, направлена на развитие социальной ниши малого и среднего бизнеса, дальнейшее формирование системы благотворительности в Новосибирской области. А поскольку мы, в первую очередь,  общественно-ориентированная организация, то почерпнули для себя много познавательного и интересного, установили новые полезные контакты». </p>
