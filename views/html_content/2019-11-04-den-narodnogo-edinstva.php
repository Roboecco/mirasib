<p>Отслужив молебен, глава Новосибирской митрополии, управляющий Новосибирской епархии Русской православной церкви, митрополит Новосибирский и Бердский Никодим сказал:<br/> 
- Сегодняшний день напоминает нам о том, как забыв о таких понятиях как честь, достоинство, забыв о вере, можно потерять свое государство. Именно так получилось вначале ХVII века, когда князья и бояре стали предавать и продавать друг друга ради власти, денег, славы. Дошло до того, что, предав и продав друг друга, они впустили иноземных захватчиков в самое сердце земли русской – в Москву, в Кремль. И не было тогда никого, кто мог бы поднять народ на защиту рубежей нашей земли.
И только голос заключенного во узы патриарха Гермогена разбудил от страха землю Русскую. Перед осадой Москвы народное ополчение стало молиться и поститься.
И как день победы в течение многих и многих веков этот день отмечался, а сейчас это государственный праздник. Ибо каждому из нас он напоминает, как все вместе мы можем одерживать победы. Когда народ един, он может прославить свою отчизну, свое государство, и в единстве нашем можем посвидетельствовать о величии Родины нашей. Господь благословит землю русскую, а мы будем трудиться достойно во славу нашего Отечества.</p>

