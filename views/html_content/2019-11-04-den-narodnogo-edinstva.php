<p>4 ноября мировцы собрались вместе с жителями Новосибирска на площади им. Пименова у Государственной публичной научно-технической библиотеки Сибирского отделения Российской Академии Наук. В 11 часов зазвучала музыка и на главной сцене площади началась историческая реконструкция событий освобождения Москвы в 1612 году народным ополчением под предводительством Кузьмы Минина и Дмитрия Пожарского.</p>

