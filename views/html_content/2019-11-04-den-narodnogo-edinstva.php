<p>После праздничного молебна жителей города поздравил полномочный представитель президента РФ в СФО Сергей Меняйло:<br/>
- День народного единства - один из самых главных праздников для нас. Более 400 лет назад люди разных сословий, национальностей и вероисповеданий, объединившись, не допустили смуту, сохранили Россию и Российскую государственность. Только опираясь на нашу многовековую историю, наши культуру и традиции, мы можем адекватно отвечать вызовам сегодняшнего времени. И в этом наша сила. Только все вместе мы сможем продолжить то, что делали наши прадеды, деды и отцы.</p>

<p>Губернатор Новосибирской области Андрей Травников:<br/> 
- Этот праздник, пожалуй, самый молодой. Но мы уже успели привыкнуть, что 4 ноября отмечается сразу два праздника – государственный и духовный. В этом году мы решили их отметить на одной площадке, что является еще одним подтверждением, насколько важны для России эти символы. Наша область очень многонациональна, здесь проживает более 1000 народов и народностей. И за всю ее историю здесь не было никогда ни религиозных, ни межнациональных распрей и конфликтов. И я хочу пожелать, чтобы в будущем на территории нашей Новосибирской области, на территории нашей огромной страны царило согласие и единение.</p>

