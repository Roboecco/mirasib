<p>К 12 часам все было готово к проведению праздничного молебна в честь Казанской иконы Божьей Матери. В непростое для России время, Казанская икона Божьей Матери стала символом объединения представителей разных национальностей, сословий, вероисповеданий русского народа. </p>

